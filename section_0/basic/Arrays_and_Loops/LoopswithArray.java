package section_0.basic.Arrays_and_Loops;

public class LoopswithArray {
    public static void main(String[] args) {
        String[] array = { "first element", "Second element", "third element" };
        System.out.println("Printing this array");
        for (int i = 0; i < array.length; i++) {
            System.out.println(array[i]);
        }

    }
}
