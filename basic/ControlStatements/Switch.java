package basic.ControlStatements;

public class Switch {
public static void main(String[] args) {
	int i=3;
	switch(i) {
	case 1:
		System.out.println("Switch 1 is passed");
		break;
	case 2:
		System.out.println("Switch 2 is passed");
		break;
	case 3:
		System.out.println("Switch 3 is passed");
		break;
	default:
		System.out.println("Default case ");
		break;
		}
}
}
